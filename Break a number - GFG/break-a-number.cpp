class Solution{   
public:
    int mod=1e9+7;
    int waysToBreakNumber(int N){
        long long n=N;
        long long ans=(n+1)*(n+2)/2;
        ans%=mod;
        return int(ans);
    }
};

//{ Driver Code Starts.
int main() 
{ 
    int t;
    cin>>t;
    while(t--)
    {
        int N;
        cin >> N;
        Solution ob;
        cout << ob.waysToBreakNumber(N) << endl;
    }
    return 0; 
} 
