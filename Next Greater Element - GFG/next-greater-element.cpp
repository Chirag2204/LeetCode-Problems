class Solution {
  public:
    vector<long long> nextLargerElement(vector<long long> &arr, int n){
        stack<long long> st;
        st.push(-1);
        reverse(arr.begin(),arr.end());
        vector<long long> ans;
        for(long long& i:arr){
            while(st.top()!=-1 and i>=st.top())st.pop();
            ans.push_back(st.top());
            st.push(i);
        }
        reverse(ans.begin(),ans.end());
        return ans;
    }
};

