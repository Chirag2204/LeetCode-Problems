class Solution{
	
	public:
	vector<int> downwardDigonal(int N, vector<vector<int>> A)
	{
		vector<int> ans;
		for(int i=0;i<2*N;i++){
		    int k=i,j=0;
		    while(k>=0){
		        if( k>=0 and k<N and j>=0 and j<N)ans.push_back(A[j][k]);
		        k--;
		        j++;
		    }
		}
		return ans;
	}

};

